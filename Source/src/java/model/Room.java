package model;

/**
 *
 * @author MinhDuy
 */
public class Room {
    private int roomID;
    private String name;

    public Room() {
    }

    public Room(int roomID, String name) {
        this.roomID = roomID;
        this.name = name;
    }

    public int getRoomID() {
        return roomID;
    }

    public void setRoomID(int roomID) {
        this.roomID = roomID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
